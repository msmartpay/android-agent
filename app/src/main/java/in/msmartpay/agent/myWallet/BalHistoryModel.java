package in.msmartpay.agent.myWallet;


class BalHistoryModel {

    public String dateBal;
    public String timeBal;
    public String modeBal;
    public String amountBal;
    public String statusBal;
    public String refIdBal;


    public String getDateBal() {
        return dateBal;
    }

    public void setDateBal(String dateBal) {
        this.dateBal = dateBal;
    }

    public String getTimeBal() {
        return timeBal;
    }

    public void setTimeBal(String timeBal) {
        this.timeBal = timeBal;
    }

    public String getModeBal() {
        return modeBal;
    }

    public void setModeBal(String modeBal) {
        this.modeBal = modeBal;
    }

    public String getAmountBal() {
        return amountBal;
    }

    public void setAmountBal(String amountBal) {
        this.amountBal = amountBal;
    }

    public String getStatusBal() {
        return statusBal;
    }

    public void setStatusBal(String statusBal) {
        this.statusBal = statusBal;
    }

    public String getRefIdBal() {
        return refIdBal;
    }

    public void setRefIdBal(String refIdBal) {
        this.refIdBal = refIdBal;
    }
 }
