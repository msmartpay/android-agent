package in.msmartpay.agent.network.model.commission;

public class ApesModel {
    String service;
    String operstor;
    String range;
    String comm;

    public String getComm() {
        return comm;
    }

    public void setComm(String comm) {
        this.comm = comm;
    }

    public String getService() {
        return service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getOperstor() {
        return operstor;
    }

    public void setOperstor(String operstor) {
        this.operstor = operstor;
    }

    public String getRange() {
        return range;
    }

    public void setRange(String range) {
        this.range = range;
    }


}
